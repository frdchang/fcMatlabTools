%% ZVEC EXIST
% If the base workspace has a loaded zvec this script will assign an
% identical variable in the caller's workspace.

evalin('base', ...
[   'if(exist(''zvec'', ''var''))', ...
    '    assignin(''caller'', ''zvec'', im_stack);', ...
    'else', ...
    '    assignin(''caller'', ''zvec'', []);', ...
    'end'   ]);